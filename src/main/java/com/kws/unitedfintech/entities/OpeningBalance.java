package com.kws.unitedfintech.entities;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

import lombok.Data;

@Entity
@Table(name = "openingBalance")
@Data
public class OpeningBalance implements Serializable {

	private static final long serialVersionUID = 1L;
	@Id
	@Column(name = "opening_id")
	public String openingId;
	@Column(name = "fin")
	public String fin;
	@Column(name = "majorHeadNature")
	public String majorHeadNature;
	@Column(name = "sub_name")
	public String subName;
	@Column(name = "cd_type")
	public String cdType;
	@Column(name = "opening_amount")
	public String openingAmount;
	@Column(name = "admin_id")
	public String adminid;

}
